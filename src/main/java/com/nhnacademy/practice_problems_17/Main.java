package com.nhnacademy.practice_problems_17;

public class Main {
    public static void main(String[] args) {
        //###연습: switch-case 문
        //####문제 17: 다음 코드의 출력 결과를 작성하시오.

        int num = 2;
        switch (num) {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("Other");

        // 정답 : Two
}
    }
}
